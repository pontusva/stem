import * as React from "react";

import { cn } from "@/lib/utils";

export interface InputProps
  extends React.InputHTMLAttributes<HTMLInputElement> {}

const Input = React.forwardRef<HTMLInputElement, InputProps>(
  ({ className, type, ...props }, ref) => {
    return (
      <input
        type={type}
        className={cn(
          "flex h-11 w-full rounded-2xl border-[1.5px] border-border bg-input px-4 py-2 text-sm font-medium transition-all duration-200 file:border-0 file:bg-transparent file:text-sm file:font-semibold file:text-[var(--blue-deep)] placeholder:text-muted-foreground/70 focus-visible:outline-none focus-visible:border-[var(--blue-deep)] focus-visible:ring-4 focus-visible:ring-ring/40 disabled:cursor-not-allowed disabled:opacity-50",
          className,
        )}
        ref={ref}
        {...props}
      />
    );
  },
);
Input.displayName = "Input";

export { Input };
